// Sort by date to get nearest matches
export function getNearestMatches(count: number): Match[] {
  return [...ALL_ARAB_MATCHES]
    .sort((a, b) => new Date(a.date).getTime() - new Date(b.date).getTime())
    .slice(0, count);
}

export function getMatchesByTeam(teamCode: string): Match[] {
  return ALL_ARAB_MATCHES.filter(
    (m) => m.homeCode === teamCode || m.awayCode === teamCode
  );
}
